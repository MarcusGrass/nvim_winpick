pub(crate) fn char_to_lines(ch: char) -> anyhow::Result<&'static [&'static str]> {
    match ch {
        'a' | 'A' => Ok(A),
        'b' | 'B' => Ok(B),
        'c' | 'C' => Ok(C),
        'd' | 'D' => Ok(D),
        'e' | 'E' => Ok(E),
        'f' | 'F' => Ok(F),
        'g' | 'G' => Ok(G),
        'h' | 'H' => Ok(H),
        'i' | 'I' => Ok(I),
        'j' | 'J' => Ok(J),
        'k' | 'K' => Ok(K),
        'l' | 'L' => Ok(L),
        'm' | 'M' => Ok(M),
        'n' | 'N' => Ok(N),
        'o' | 'O' => Ok(O),
        'p' | 'P' => Ok(P),
        'q' | 'Q' => Ok(Q),
        'r' | 'R' => Ok(R),
        's' | 'S' => Ok(S),
        't' | 'T' => Ok(T),
        'u' | 'U' => Ok(U),
        'v' | 'V' => Ok(V),
        'w' | 'W' => Ok(W),
        'x' | 'X' => Ok(X),
        'y' | 'Y' => Ok(Y),
        'z' | 'Z' => Ok(Z),
        '0' => Ok(ZERO),
        '1' => Ok(ONE),
        '2' => Ok(TWO),
        '3' => Ok(THREE),
        '4' => Ok(FOUR),
        '5' => Ok(FIVE),
        '6' => Ok(SIX),
        '7' => Ok(SEVEN),
        '8' => Ok(EIGHT),
        '9' => Ok(NINE),
        ';' => Ok(SEMI_COLON),
        // Todo: More instructive error, but should check on config that all chars are valid
        unk => anyhow::bail!("char {unk} has no mapped ASCII art representation"),
    }
}
